package model;

public class Parametre {
	
	protected float nbHeure;
	protected float nbElement;
	protected float minParam;
	protected float maxParam;
	
	
	public float getNbHeure() {
		return nbHeure;
	}
	public void setNbHeure(float nbHeure) {
		this.nbHeure = nbHeure;
	}
	public float getNbElement() {
		return nbElement;
	}
	public void setNbElement(float nbElement) {
		this.nbElement = nbElement;
	}
	public float getMinParam() {
		return minParam;
	}
	public void setMinParam(float minParam) {
		this.minParam = minParam;
	}
	public float getMaxParam() {
		return maxParam;
	}
	public void setMaxParam(float maxParam) {
		this.maxParam = maxParam;
	}

	

}
